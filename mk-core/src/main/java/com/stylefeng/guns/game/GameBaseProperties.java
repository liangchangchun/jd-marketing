package com.stylefeng.guns.game;


public class GameBaseProperties {
	
	String validateToken;
	String consumeGame;
	String endRound;
	String endGame;
	String machineIp;
	Integer machinePort;
	
	public String getValidateToken() {
		return validateToken;
	}
	public void setValidateToken(String validateToken) {
		this.validateToken = validateToken;
	}
	public String getConsumeGame() {
		return consumeGame;
	}
	public void setConsumeGame(String consumeGame) {
		this.consumeGame = consumeGame;
	}
	public String getEndRound() {
		return endRound;
	}
	public void setEndRound(String endRound) {
		this.endRound = endRound;
	}
	public String getEndGame() {
		return endGame;
	}
	public void setEndGame(String endGame) {
		this.endGame = endGame;
	}
	public String getMachineIp() {
		return machineIp;
	}
	public void setMachineIp(String machineIp) {
		this.machineIp = machineIp;
	}
	public Integer getMachinePort() {
		return machinePort;
	}
	public void setMachinePort(Integer machinePort) {
		this.machinePort = machinePort;
	}
	
	
	
}
